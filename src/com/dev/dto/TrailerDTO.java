package com.dev.dto;

public class TrailerDTO {
	private int trailnum;
	private String video;
	private String vname;
	private String vactor;
	private String vopen;
	private String vgenre;
	public int getTrailnum() {
		return trailnum;
	}
	public String getVideo() {
		return video;
	}
	public String getVname() {
		return vname;
	}
	public String getVactor() {
		return vactor;
	}
	public String getVopen() {
		return vopen;
	}
	public String getVgenre() {
		return vgenre;
	}
	public void setTrailnum(int trailnum) {
		this.trailnum = trailnum;
	}
	public void setVideo(String video) {
		this.video = video;
	}
	public void setVname(String vname) {
		this.vname = vname;
	}
	public void setVactor(String vactor) {
		this.vactor = vactor;
	}
	public void setVopen(String vopen) {
		this.vopen = vopen;
	}
	public void setVgenre(String vgenre) {
		this.vgenre = vgenre;
	}
}
